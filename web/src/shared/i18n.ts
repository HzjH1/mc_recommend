export function formatText(template: string, values: Record<string, string | number>) {
  return Object.entries(values).reduce((text, [key, value]) => {
    return text.replaceAll(`{${key}}`, String(value));
  }, template);
}

export function getTranslations(language?: string) {
  return translations[language === 'en-US' ? 'en-US' : 'zh-CN'];
}

export function getTabBarTexts(language?: string) {
  const common = getTranslations(language).common;
  return [
    { path: '/home', text: common.homeTab },
    { path: '/menu', text: common.meicanTab },
    { path: '/mine', text: common.meTab },
  ];
}
